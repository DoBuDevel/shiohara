                    <p dir="ltr"><a href="http://www.figurama.com.br/2013/06/10/critica-qotsa-x-like-clockwork/qotsa-likeclockwork-figurama/" rel="attachment wp-att-12169"><img class="alignnone size-full wp-image-12169" alt="QOTSA -...like clockwork" src="http://www.figurama.com.br/wp-content/uploads/2013/06/qotsa-likeclockwork-figurama.jpg" width="570" height="570" /></a></p>
<p dir="ltr">Fala galera! Hoje vamos à mais uma crítica de álbum (ainda sinto o peso da palavra “crítica”). Pra quem ainda não viu nenhuma dessas críticas, a idéia é: avaliar discos ou bandas que não costumo ouvir, ou que nem mesmo conheço! Eu diria que são críticas de “primeira ouvida”.</p>
<p>E o álbum de hoje é o “<strong>&#8230;Like Clockwork</strong>”, do <a href="http://www.qotsa.com/" target="_blank">Queens of the Stone Age</a> (QOTSA partindo daqui). Essa pelo menos eu já tinha ouvido falar e ouvido algo deles. Meu conhecimento sobre a banda se resume a: 1) “Ah, é aquela banda do cara de barbicha que entrou pelado em algum show no Brasil e rolou algum problema com a polícia”; 2) “Ah, é aquela banda que o tem um clipe com o Dave Grohl”. Sim, só isso. Bora pra review.</p>
<ol>
<li dir="ltr">
<p dir="ltr"><strong>Keep Your Eyes Peeled</strong>: Sombrio, minimalista e com uma distorção (de guitarra) bem bizarra. Me lembra quando eu plugava uma guitarra que eu tinha no som National do meu pai,<br />
e também algumas bandas da fase 70 do rock progressivo.</p>
</li>
<li dir="ltr">
<p dir="ltr"><strong>I Sat By the Ocean</strong>: A mixagem desses caras é bem crua né? Tudo muito direto, com aquele<br />
distorção característica de fita. Gostei do clima mais “estrada” dessa música, boas melodias simples mas bem colocadas.</p>
</li>
<li dir="ltr">
<p dir="ltr"><strong>The Vampyre of Time and Memory</strong>: É, acho que esses caras devem ouvir prog. 70’s. Sons de sintetizador analógico, e um piano bem artificial. Uma pena a cadência do piano ser bem clichê (o famoso Im | Im/VII | Im/VI). Mas apesar disso, é boa!</p>
</li>
<li dir="ltr">
<p dir="ltr"><strong>If a Had a Tail</strong>: Reverb de mola, dá-lhe! O vocal remete à Joy Division (o que não é algo muito bom na minha opinião). É a música mais nervosa do disco! Finalzinho obscuro, como a música.</p>
</li>
<li dir="ltr">
<p dir="ltr"><strong>My God is the Sun</strong>: Pô, essa é boa! É bacana o jeito que ela progride, e essas percussões rolando incessantemente. Melodia foda do vocal, backings sombrios e arrepiantes! Sem dúvida, a melhor música do disco! Que demais.</p>
</li>
<li dir="ltr">
<p dir="ltr"><strong>Kalopsia</strong>: A música anterior elevou muito o disco, ficou difícil. Mas essa manteve a qualidade melódica, harmônica e timbristica dela. Que boa surpresa!</p>
</li>
<li dir="ltr">
<p dir="ltr"><strong>Fairweather Friends</strong>: Esse quase mellotron do começo eu gostei! Pô, o álbum fica foda à partir de “My God is the Sun” ou é impressão minha? Outra música incrível! O ritmo meio “tercinado” e os solos aproveitando as melhores notas sobre os acordes é incrível! Emocionei.</p>
</li>
<li dir="ltr">
<p dir="ltr"><strong>Smooth Sailing</strong>: Ahhhhhhhhhh não&#8230; Porque depois de três músicas incríveis vem&#8230; Enfim. Aproveitando pelo menos um pedaço dela, o trecho 3:48 até 4:09 é legal.</p>
</li>
<li dir="ltr">
<p dir="ltr"><strong>I Appear Missing</strong>: Hummm, aproveitando o mesmo clichê harmônico que comentei anteriormente. Infelizmente, não deu barato.</p>
</li>
<li dir="ltr">
<p dir="ltr"><strong>&#8230;Like Clockwork</strong>: Ufa! Essa fecha bem o disco, muito bem construída! No nível das melhores do disco. Tem uma coisa floydiana nela que criou um clima muito bom!</p>
</li>
</ol>
<p><iframe src="http://www.youtube.com/embed/RjYZToXuJaM" height="321" width="570" allowfullscreen="" frameborder="0"></iframe></p>
<p>Acho que me tornei fã “parcialmente” do “&#8230;Like Clockwork” e do QOTSA. Se o disco se resumisse às faixas 5 (My God is the Sun), 6 (Kalopsia), 7 (Fairweather Friends) e 10 (&#8230;Like Clockwork), poderia me declarar convertido. Mas hoje, me declaro interessado. Gostei da maioria dos timbres, dessa cara setentista na mixagem e desse clima sombrio (ótima trilha pro filme Ed Wood e só consigo imaginar o rosto do Bela Lugosi por detrás da máscara do drácula da capa).</p>
<p>E o que eu estava mais ouvindo quando ouvi o “&#8230;Like Clockwork”, do QOTSA? Era “Pat Metheny Unity Band”, a reunião de mestres que o Pat Metheny conseguiu.</p>
