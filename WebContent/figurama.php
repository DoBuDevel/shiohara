                    <p><a href="http://www.figurama.com.br/2013/05/27/critica-daft-punk-x-random-access-memories/daftpunk-randomacessmemories/" rel="attachment wp-att-12140"><img class="alignnone size-full wp-image-12140" alt="Crítica - Random Acess Memories X Daft Punk" src="http://www.figurama.com.br/wp-content/uploads/2013/05/daftpunk-randomacessmemories.jpg" width="570" height="377" /></a><br />
Comentar álbuns. De música. Estranho nunca ter feito isso, pois ouço, estudo e pesquiso muita música. Nunca fiz porque tinha medo de ser injusto, pois já li críticas horríveis (ou melhor, que eu não concordava) sobre álbuns que amava. É, não é uma tarefa fácil.</p>
<p>A coisa mais legal neste desafio é avaliar bandas “à primeira ouvida”, ou mesmo ouvir o que eu não costumo ouvir. Nessa estréia, vou avaliar o último disco da <a href="http://www.daftpunk.com/" target="_blank">Daft Punk</a>, chamado “<strong>Random Access Memories</strong>”. Sim, R.A.M.! Sinceramente não conhecia nada de Daft Punk (pelo menos que eu saiba!), então esse álbum vai me apresentar os caras. Chega de papo e vamos lá!</p>
<ol start="1">
<li><strong>Give Life Back to Music</strong>: Opa, peraí, Daft Punk não era música eletrônica? Surpresa a minha ouvindo timbres bastante “acústicos”, um ritmo funkeado e dançante. Praticamente um tributo à disco circa 70.</li>
<li><strong>The Game of Love</strong>: Quase uma balada pro pré-coito. Muito bacana os vocoders e o clima quase sexual da música, as intervenções de algo parecido com um rhodes vitaminado com muito reverb e as guitarras trabalhadas no wah-wah. É, o funk parece que vai permear o álbum inteiro.</li>
<li><strong>Giorgio by Moroder</strong>: Hein? Não entendi não. Uma faixa só com silêncio nessa altura do campeonato?</li>
<li><strong>Within</strong>: Wow! Bonito piano de introdução, cadências muito interessantes. E mais vocoder, com uma melodia bastante marcante. Sinto a presença de um baterista de verdade (nunca se sabe). Ahhhhhh, mas acaba meio decepcionante. A música poderia ter “crescido”.</li>
<li><strong>Instant Crush</strong>: Fãs de Daft Punk, vocês me perdoam? Achei bastante repetitiva essa música, sem grande momentos. Mas pra não sair zerado, ela tem um solo de guitarra interessante, com umas dobrinhas e uma cara de  amp Fender + Fuzz Face danada.</li>
<li><strong>Lose Yourself to Dance</strong>: Olha o funk aí de novo, com uns slaps de baixo discretos porém legais! Dessa vez, vocal sem vocoder! Imagino perfeitamente meus pais se encontrando na “discoteca”, calça boca de sino, bolas espelhadas girando, luzes coloridas e todos batendo palma! Essa até eu dançaria. C’mon, c’mon, c’mon, c’mon!</li>
<li><strong>Touch</strong>: Dá-lhe sintetizador analógico! Dá-lhe atmosfera ácida! E você acorda num musical naipe “Jesus Christ Superstar”. Eu já imagino Jesus Cristo dançando à la Tony Manero. Diria que essa é a música mais “progressiva” até agora. Melhor música do álbum (preciso dizer “na minha opinião?”)!</li>
<li><strong>Get Lucky</strong>: Peraí, essa música eu já ouvi em algum lugar! Mas gostei não. Ela é muito cíclica. Mas nem tudo está perdido! Lá pelo final aparece umas intervenções vocais com cara de vocoder e Synclavier.</li>
<li><strong>Beyond</strong>: Que introdução incrível! Mais funk e vocoder. Hum&#8230;, de novo?</li>
<li><strong>Motherboard</strong>: Instrumental, ritmo muito bacana, cordas bem pontuadas, cadências e arpejos de muito bom gosto. Musica boa, muito boa! Lembra um pouco os climas “Zero7inianos”. Segunda melhor música do disco.</li>
<li><strong>Fragments of Time</strong>: Um crossover de Jamiroquai com Djavan, rhodes e grooveira da boa, solos com timbre moogiano.</li>
<li><strong>Doin’ it right</strong>: Já aqui, uma base rítmica com algo parecido com as famosas claps e hi-hatsdo TR-808, e uma repetição quase hipnótica de frases sobrepostas.</li>
<li><strong>Contact</strong>: Um fechamento não funk pra um disco bem funk. Música simples, mais um conceito do que qualquer outra coisa. O glissando bem lento do final é pouco angustiante, e é difícil saber pra onde ele vai te levar. E sim, ele te leva pro final do álbum.</li>
</ol>
<p><iframe src="http://www.youtube.com/embed/v3pvx-6nSGE" height="321" width="570" allowfullscreen="" frameborder="0"></iframe><br />
Ao término do álbum, não me tornei um fã imediato de Daft Punk, mas me aguçou a tentar conhecer mais, principalmente pelas referências eletrônicas setentistas, pelo groove e pela experimentações bastante conceituais.</p>
<p>E o que eu estava mais ouvindo quando ouvi o R.A.M., do Daft Punk? Era “Themata”, o primeiro álbum dos australianos do Karnivool.</p>
                

              



